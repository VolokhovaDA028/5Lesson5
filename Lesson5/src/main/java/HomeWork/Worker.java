package HomeWork;

public class Worker {
    // Объекты класса
     String fullName;
     String position;
     String email;
     String phoneNamber;
     int salary;
     int age;

    // Конструктор
    public Worker(String fullName, String position, String email, String phoneNamber, int salary, int age) {
        this.fullName = fullName;
        this.position = position;
        this.email = email;
        this.phoneNamber = phoneNamber;
        this.salary = salary;
        this.age = age;
    }
}
